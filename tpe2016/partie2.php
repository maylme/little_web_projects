    <h2>2.2. Les représentations Filmographiques</h2>
    <p>
    Le cinéma afro-américain sera fortement marqué par la Ségrégation, et de nombreuses figures vont utiliser le cinéma pour témoigner, tel Edison en 1898 avec <strong>The Colored Troops Disembarking</strong> et <strong>The Ninth Negro Cavalry Watering Horses</strong> et en 1904 avec <strong>Ten Pickaninnies</strong>.
    </p>
    <p>
    Même si au départ, ceux-ci sont généralement représentés de manière caricaturale et moqueuse, à partir de 1910, des réalisateurs noirs tentent, avec peu de moyens, de contrebalancer cette tradition. William J. Foster, Oscar Micheaux ou encore Spencer William, chacun à leur manière, marquent de leur empreinte l'histoire souvent difficile et semée d'obstacles des grands débuts du cinéma noir. Dans son film <strong>The colored Troops Disembarking</strong>  (1898) Edison montre des soldats noirs débarquant à Cuba alors que la propagande raciste prédomine en ce début de 20e siècle le montage n'existe pas encore ; ce qui permet aux Noirs d'apparaître à l'image tels qu'ils sont réellement. empêchant tout types de caricatures ou de trucages, cependant le théâtre et ses "blackfaces" utilise des acteurs blancs maquillés en noirs pour se moquer des Afro-américains en les faisant passer pour des demeurés, des paresseux ou des peureux. C'est pour mettre fin à cette pratique que William Jones Foster crée, en 1910, la Foster Photoplay Company .  Son film, <strong>The Railroad Porter </strong>, tourné en 1912, est le premier film réalisé par un cinéaste afro-américain. Foster est également le premier d'une lignée de cinéastes noirs à se battre pour financer leurs films. Plusieurs films dans les années 1910 - 1920 vont faire leur apparition pour dénoncer les conditions et le traitements des noirs, par exemple En 1915, sort aux Etats-Unis le film <strong>Naissance d'une nation</strong>. Traitant de la guerre de Sécession, ce film glorifie le Ku Klux Klan et son idéologie de suprématie blanche, par le cinéaste David Wark Griffith y évoque l'idée selon laquelle les Noirs seraient responsables de la désunion des Etats-Unis. A la fin de la Seconde guerre mondiale, même si les Noirs n'ont pas réussi à obtenir ce qu'ils espéraient en matière de droits civiques, le cinéma modifie quelque peu leur image. Des cinéastes blancs, comme Elia Kazan et son  <strong>Héritage de la Chair</strong>  traitent pour la première fois des problèmes liés au racisme. De nombreux autres cinéastes blancs, tels qu'Otto Preminger, permettent ainsi aux Noirs de se détacher des stéréotypes véhiculés par le cinéma américain. Un premier pas important qui va faire évoluer peu à peu les mentalités de la population américaine.
    </p>
    </section>
