    <h2>2.1 Les représentations photographiques </h2>
    <p>
    Dans un contexte où l'image à un réel pouvoir dans la lutte pour les droits civiques, comment peut-on interpréter les photographies qui dénoncent la ségrégation raciale aux Etats-Unis entre 1940 et 1970 ? 
    </p>

    <p>
    Les années 1950 marquent le début d'un combat ouvert entre les Afro-Américains qui contestent le système de la ségrégation et les blancs qui refusent de leurs donner des droits. De nombreux photographes vont se mobiliser à cette époque pour dénoncer et témoigner de l'horreur de la ségrégation et y lutter comme Gordon Parks célébre photographes, réalisateur, journaliste et militant noir Américain, ou encore le photographe et photojournaliste Américain Elliott Erwitt célèbre pour sa photographie <strong>USA. North Carolina</strong>  en 1950 :
    </p>

    <figure>
        <img src="img/lavemain.png"/>
    </figure>
    <p>
    Elle montre un homme noir buvant à un robinet qui lui est assignée, tout en jetant un oeil sur un robinet du dernier modèle, réservée aux Blancs, on peut remarquer qu'à cette époque, même les toilettes sont divisées par la ségrégation raciale. Pour Erwitt, cette prise de vue sur le vif montrait une scène « tragique » mais ordinaire :on voyait ce genre de scène partout dans les années 1950, cette image à pour but de dénoncer et pousser le peuple noir à la révolte comme Rosa Parks, c'est une véritable injustice de la ségrégation en Amérique.
    </p>

    <p>
    Gordon Parks est un photographe prosaïque, c'est à dire qu'il se préoccupe des situations réels concrètes, il oppose les conditions de vie du peuple blanc aux conditions de vies du peuple noir. L'image photographique de la ségrégation est précieuse, car elle documente au delà de son sujet.
    </p>

