    </figure>

    <p>
    Septembre 1956 : Gordon Parks publie cette photo sur Life-Magazine dénonçant les conditions de vie déplorables des familles africo-américaine à cause des lois de Jims Crows il s'agit d'une de ces photos les plus marquantes et choquantes de la ségrégation aux Etats-Unis.
    </p>
	<figure>
        <img src="img/poupe.png" />
