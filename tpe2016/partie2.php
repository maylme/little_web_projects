    <p>
    Gordon Parks est aussi connu pour sa photographie <strong>The Doll Test</strong>  en 1947.  Dans "le test de poupée," les psychologues Kenneth et Mamie Clark ont utilisé quatre poupées en plastique, vêtues de la même façon à part la couleur. Ils ont montré les poupées aux enfants noirs entre   trois et sept ans et leur ont posé des questions pour déterminer la perception raciale et leurs préférences. Presque tous les enfants ont facilement identifié la race des poupées. Cependant, une fois demandé qui ils ont préférés, la majorité a choisi la poupée blanche et y a attribué des caractéristiques positives. On leurs a aussi donné des dessins d'enfants, d'un garçon et d'une fille et on leur a demandé de colorer les personnages de la même couleur qu'eux-mêmes. Beaucoup d'enfants noirs ont colorés les personnages avec un crayon blanc ou jaune. On en a conclu que les enfants noirs ont développé un sens d'infériorité et d'auto-haine liée à la ségrégation.  Cette photographie a été prise par Gordon Parks pour une publication dans un magazine.
    </p>
