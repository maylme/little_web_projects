    <h2> 2.3.  Les autres représentations dans l'art. </h2>

    <p>
    En plus des représentations photographiques et filmographiques, les autres représentations artistiques vont jouer un rôle important dans le développement de la ségrégation aux Etats-Unis.
    </p>
    <p>
    Comme la peinture à l'huile sur toile peint par Norman Rockwell en 1967. Ses dimensions sont de 91,5 cm x 146 cm et elle est exposée au Norman Rockwell Museum, à Stockbridge, Massachussetts ( Etats-Unis ).Elle à été faite pour illustrer un article dans le magazine « Look » du 16 mai 1967.
    </p>

    <figure>
        <img src="img/enfant.png"/>
    </figure>
    <p>
    Elle appartient au genre Hyper-réaliste, c'est un mouvement artistique américain des années1950 à 1960. Il consiste à la reproduction à l'identique d'une photographie mais en peinture. Deux groupes d'enfants se font faces et s'observent. A gauche, un garçon et une petite fille tout deux noirs et à droite, deux garçons et une fillette tout les trois blancs. Ils sont habillés en clair pour un aspect positif de la situation. Le tableau dénonce le racisme noirs/blancs et la difficulté d'imposer la déségrégation aux américains. En 1960, Ruby Bridges devint la 1ère enfant afro-américaine à intégrer une école blanche. On peut voir que les enfants blancs ont franchi une des lignes au sol alors que les noirs eux sont encore derrière une ligne. Ils veulent surement sympathiser et Norman Rockwell a voulu montrer que c'est peut être les enfants qui vont combattre la différence raciale. Au premier-plan, un grand vides espace les deux groupes il symbolise l'inconnu et une ligne les séparant pouvant symboliser le manque de contact entre les deux cultures .
    </p>
    <p>
Ou encore la peinture <strong>The problem we all live with</strong>  symbolisant l'Etape de déségrégation aux Etats-Unis.
    </p>
    <p>
    Il s'agit d'une huile sur toile de dimension 91,4 x 147,3 cm ( 36 x 58 pouces ). Elle est conservée dans l'aile ouest de la Maison Blanche à Washington.
    </p>
    <figure>
        <img src="img/petitefille.png"/>
    </figure>
    <p>
    Cette œuvre de Norman Rockwell rend hommage au premier jour d'école de "Ruby Bridges". Alors âgée de seulement de 4 ans, Ruby est la première enfant afro-américaine à fréquenter une école blanche. Norman Rockwell relate cet évènement en montrant une petite fille vêtue de blanc, elle est escortée par quatre "Marshalls" fédéraux, représentant l'autorité, qui la protègent des harcèlements des opposants à la déségrégation. On peut observer les projectiles dont Ruby Bridges est la cible, comme la tomate éclatée contre le mur. Le rouge de la tomate symbolise le sang et les éclaboussures figurent la violence.
    </p>

